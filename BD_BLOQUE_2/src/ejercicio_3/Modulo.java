package ejercicio_3;

public class Modulo{
	
	public String id_modulo;
	public String nombre;
	public String curso;
	public String horas;
	public String profesor;
	
	public Modulo(String id_modulo, String nombre, String curso, String horas, String profesor) {
		
		this.id_modulo=id_modulo;
		this.nombre=nombre;
		this.curso=curso;
		this.horas=horas;
		this.profesor=profesor;
		
	}

}
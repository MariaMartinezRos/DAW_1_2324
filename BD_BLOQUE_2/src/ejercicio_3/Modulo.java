package ejercicio_3;

public class Modulo{
	
	public String id;
	public String nombre;
	public String curso;
	public String horas;
	public String profesor;
	
	public Modulo(String id, String nombre, String curso, String horas, String profesor) {
		
		this.id=id;
		this.nombre=nombre;
		this.curso=curso;
		this.horas=horas;
		this.profesor=profesor;
		
	}

}
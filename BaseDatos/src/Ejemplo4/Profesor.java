package Ejemplo4;

public class Profesor{
	
	public String nrp;
	public String dni;
	public String nombre;
	public String apellidos;
	public String departamento;
	
	public Profesor(String nrp, String dni, String nombre, String apellidos, String departamento) {
		
		this.nrp = nrp;
		this.dni = dni;
		this.nombre = nombre;
		this.apellidos = apellidos;
		this.departamento = departamento;
		
	}
	
}